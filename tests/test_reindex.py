# ─── Idempotence ─────────────────────────────────────────────────────────────


class TestIdempotence:
    def test_sessions_idempotent(self, tmp_path: Path):
        _write_session(tmp_path, "claude-20260421-1430", date(2026, 4, 21))
        main(["--sessions", "--root", str(tmp_path)])
        first = (tmp_path / "_meta" / "sessions" / "INDEX.md").read_text(
            encoding="utf-8"
        )
        main(["--sessions", "--root", str(tmp_path)])
        second = (tmp_path / "_meta" / "sessions" / "INDEX.md").read_text(
            encoding="utf-8"
        )
        assert first == second

    def test_all_idempotent(self, tmp_path: Path):
        _write_session(tmp_path, "claude-20260421-1430", date(2026, 4, 21))
        concepts_root = tmp_path / "_meta" / "concepts"
        concepts_root.mkdir(parents=True)
        write_concept(_make_concept(), concepts_root)

        main(["--all", "--root", str(tmp_path)])
        s1 = (tmp_path / "_meta" / "sessions" / "INDEX.md").read_text(encoding="utf-8")
        c1 = (tmp_path / "_meta" / "concepts" / "INDEX.md").read_text(encoding="utf-8")

        main(["--all", "--root", str(tmp_path)])
        s2 = (tmp_path / "_meta" / "sessions" / "INDEX.md").read_text(encoding="utf-8")
        c2 = (tmp_path / "_meta" / "concepts" / "INDEX.md").read_text(encoding="utf-8")

        assert s1 == s2
        assert c1 == c2


# ─── Error handling ──────────────────────────────────────────────────────────


class TestErrorHandling:
    def test_malformed_frontmatter_skipped_not_fatal(self, tmp_path: Path):
        sessions_dir = tmp_path / "_meta" / "sessions"
        sessions_dir.mkdir(parents=True)
        # A well-formed entry
        _write_session(tmp_path, "claude-20260421-1430", date(2026, 4, 21))
        # A file with malformed YAML — should be skipped, not crash the tool
        (sessions_dir / "bad.md").write_text(
            "---\nnot: [valid: yaml\n---\nbody\n", encoding="utf-8"
        )
        rc = main(["--sessions", "--root", str(tmp_path)])
        assert rc == 0
        content = (sessions_dir / "INDEX.md").read_text(encoding="utf-8")
        # The good one is indexed
        assert "claude-20260421-1430" in content

    def test_verbose_accepted(self, tmp_path: Path):
        _write_session(tmp_path, "claude-20260421-1430", date(2026, 4, 21))
        rc = main(["--sessions", "--verbose", "--root", str(tmp_path)])
        assert rc == 0


