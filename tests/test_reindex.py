"""Tests for tools.reindex — unified session + concept index entry point.

Per references/tools-spec.md §6.1. reindex.py is a thin CLI wrapper that
delegates to cortex helpers (session_index.rebuild_index and
concept.rebuild_concept_index) — the behavioural contracts here focus on
the CLI: flag parsing, exit codes, output files, and idempotence.
"""

from __future__ import annotations

from datetime import date, datetime
from pathlib import Path

from tools.lib.cortex.concept import write_concept
from tools.lib.cortex.session_index import session_summary_to_markdown
from tools.lib.second_brain import (
    Concept,
    ConceptProvenance,
    SessionSummary,
)
from tools.reindex import main

# ─── Fixtures ───────────────────────────────────────────────────────────────


def _make_summary(
    session_id: str = "claude-20260421-1430",
    date_val: date = date(2026, 4, 21),
) -> SessionSummary:
    return SessionSummary(
        session_id=session_id,
        date=date_val,
        started_at=datetime.combine(date_val, datetime.min.time()).replace(hour=14),
        ended_at=datetime.combine(date_val, datetime.min.time()).replace(
            hour=14, minute=30
        ),
        duration_minutes=30,
        platform="claude",
        theme="zh-classical",
        project="life-os",
        workflow="full_deliberation",
        subject="test",
        overall_score=8.5,
        veto_count=0,
        council_triggered=False,
        compliance_violations=0,
        body="body",
        keywords=["k1", "k2"],
    )


def _write_session(root: Path, session_id: str, date_val: date) -> Path:
    sessions_dir = root / "_meta" / "sessions"
    sessions_dir.mkdir(parents=True, exist_ok=True)
    s = _make_summary(session_id=session_id, date_val=date_val)
    path = sessions_dir / f"{session_id}.md"
    path.write_text(session_summary_to_markdown(s), encoding="utf-8")
    return path


def _make_concept(concept_id: str = "strategy", domain: str = "professional") -> Concept:
    return Concept(
        concept_id=concept_id,
        canonical_name=concept_id.replace("-", " ").title(),
        domain=domain,
        status="confirmed",
        permanence="skill",
        activation_count=5,
        last_activated=datetime(2026, 4, 20, 12, 0),
        created=datetime(2026, 1, 1, 12, 0),
        body="A test concept.",
        aliases=[],
        outgoing_edges=[],
        provenance=ConceptProvenance(source_sessions=["claude-20260421-1430"]),
    )


# ─── --sessions flag ─────────────────────────────────────────────────────────


class TestSessionsOnly:
    def test_writes_session_index_only(self, tmp_path: Path):
        _write_session(tmp_path, "claude-20260421-1430", date(2026, 4, 21))
        rc = main(["--sessions", "--root", str(tmp_path)])
        assert rc == 0
        idx = tmp_path / "_meta" / "sessions" / "INDEX.md"
        assert idx.exists()
        # Concepts INDEX not written because --sessions only
        assert not (tmp_path / "_meta" / "concepts" / "INDEX.md").exists()

    def test_index_header_has_generator_marker(self, tmp_path: Path):
        _write_session(tmp_path, "claude-20260421-1430", date(2026, 4, 21))
        rc = main(["--sessions", "--root", str(tmp_path)])
        assert rc == 0
        content = (tmp_path / "_meta" / "sessions" / "INDEX.md").read_text(
            encoding="utf-8"
        )
        assert "generated by tools/reindex.py" in content

    def test_empty_sessions_dir_exits_zero(self, tmp_path: Path):
        (tmp_path / "_meta" / "sessions").mkdir(parents=True)
        rc = main(["--sessions", "--root", str(tmp_path)])
        assert rc == 0


